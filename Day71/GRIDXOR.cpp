int main(){
    int t;
    cin>>t;
    
    while(t--){
        int n;
        cin>>n;
        
        int d=0;
        while(d<n){
        for(int i=0;i<n;++i){
            cout<<1<<" ";
        }
        cout<<"\n";
        d++;
        }
        
    }
}
